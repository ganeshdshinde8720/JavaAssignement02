package assignment01;

public class Swap2NumberDemo {

	public static void main(String[] args) {
		int a=40;
		int b=25;
		
		System.out.println(a);
		System.out.println(b);
		
		int temp =a;
		a = b;
		b = temp;
		
		System.out.println("after swapping");
		System.out.println(a);
		System.out.println(b);

	}

}
