package assignment01;

public class ArrayDemo {

	public static void main(String[] args) {
		
		int a[]= { 10,20,30,40,50,60};
		char ch[]= {'a','e','i','o','u'};
		String str[]= {"java","python","ruby","PHP","react","selenium"};
		
		System.out.println(a[4]);  // 50
		System.out.println(a[2]);  // 30
		System.out.println(ch[3]); // o
		System.out.println(ch[0]); // a
		System.out.println(str[2]);// ruby
		System.out.println(str[5]);// selenium
		

	}

}
